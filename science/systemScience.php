				<p>There are several definitions of “applied system science”. So let me show you, what does the term to my mind mean and how does those studies look like, because this study path is rather unusual.
				I use the term “system science” according to “general system theory” or “Allgemeine Systemtheorie” (Ludwig von Bertalanffy). In my view, it makes sense to consider the general system theory as theory, because applied system science links theory and practice. System science is a metascience, that means system science can’t be assigned to faculties like natural science, humanities, mathematics/computer science. Its classification reminds me of the subsumption of the mathematics. Mathematics are independent, but used in several disciplines economy, ecology or sociology. In this sense, is system science interdisciplinary and owns  its subjects like autopoiesis, self organisation, synergetic and chaos.
				Furthermore, I would like to contradict that system science is part of mathematics, although it is using methods from mathematics, because there are representatives of the system science, which do not use mathematics for formalisation of scientific recognition - for example the theory of “Niklas Luhmann”.
				However, what exactly is a system? A system is a set of objects, its relations and the so called system-environment -difference, that means the border, which separates system and environment. Of course a system has not only a structure, it is subjected to processes like evolution serving system preservation and integrity. Which aspects of reality are counted among the system and which belongs the environment, depends on the scientific question and perspective. So it is obvious, that the fact, whether an object belongs to a system or not, is not property of the object itself. It is a matter of construction as well as any science.
				So system science is looking for a theories, which are basically valid for systems, independent of the kind of its components and relations.</p>
				
